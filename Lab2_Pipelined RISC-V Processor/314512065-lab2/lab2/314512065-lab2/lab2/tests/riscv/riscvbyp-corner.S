#include "riscv-macros.h"

        TEST_RISCV_BEGIN

//----------------------------------------------------------------------
// B1) EX→EX：上一條 ALU 結果，下一條 ALU 直接使用（零距離）
// 目的：驗證 EX 階段輸出能立刻旁路給下一條指令的來源運算元。
// 期望：x6 = 3（若沒旁路，容易讀到舊值或需要不必要的 stall）
//----------------------------------------------------------------------
        addi  x3, x0, 1           // x3 = 1
        addi  x4, x0, 2           // x4 = 2
        add   x2, x3, x4          // x2 = 3    （於 EX 產生）
        add   x6, x2, x0          // 立刻使用 x2 的結果（需 EX→EX bypass）
        TEST_CHECK_EQ( x6, 3 )

//----------------------------------------------------------------------
// B2) MEM→EX：load-use（距離 1），不靠 stall，應靠旁路
// 目的：load 的資料在 MEM/WB 才可用；旁路應把該值直接供給下一條 EX。
// 期望：x6 = 0x000000ff
//----------------------------------------------------------------------
        la    x1, tdata_0
        lw    x5, 0(x1)           // x5 = 0x000000ff （資料於 MEM 完成）
        add   x6, x5, x0          // 緊接使用（需 MEM→EX bypass，而非插泡）
        TEST_CHECK_EQ( x6, 0x000000ff )



//-------------------------------------------------------------------------
// Test data
//-------------------------------------------------------------------------
        .section .data
        .align 4

tdata_0:  .word 0x000000ff
tdata_1:  .word 0x00007f00
tdata_2:  .word 0x00000ff0
tdata_3:  .word 0x0000700f

// 額外測試緩衝區（避免動到原本 tdata_* 固定測資）
        .align 4
storebuf: .word 0,0,0,0
